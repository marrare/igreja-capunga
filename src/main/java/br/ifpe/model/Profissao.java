package br.ifpe.model;

public class Profissao {

	private String ocupacao;
	private String formacao;
	private String area;
	private String empresa;
	private String setor;
	private String enderecoTrabalho;
	private String bairroTrabalho;
	private String cidadeTrabalho;
	private String estadoTrabalho;
	private String cepTrabalho;
	private TipoProfissao tipoProfissao;
	
	
	public String getOcupacao() {
		return ocupacao;
	}
	public void setOcupacao(String ocupacao) {
		this.ocupacao = ocupacao;
	}
	public String getFormacao() {
		return formacao;
	}
	public void setFormacao(String formacao) {
		this.formacao = formacao;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getSetor() {
		return setor;
	}
	public void setSetor(String setor) {
		this.setor = setor;
	}
	public String getEnderecoTrabalho() {
		return enderecoTrabalho;
	}
	public void setEnderecoTrabalho(String enderecoTrabalho) {
		this.enderecoTrabalho = enderecoTrabalho;
	}
	public String getBairroTrabalho() {
		return bairroTrabalho;
	}
	public void setBairroTrabalho(String bairroTrabalho) {
		this.bairroTrabalho = bairroTrabalho;
	}
	public String getCidadeTrabalho() {
		return cidadeTrabalho;
	}
	public void setCidadeTrabalho(String cidadeTrabalho) {
		this.cidadeTrabalho = cidadeTrabalho;
	}
	public String getEstadoTrabalho() {
		return estadoTrabalho;
	}
	public void setEstadoTrabalho(String estadoTrabalho) {
		this.estadoTrabalho = estadoTrabalho;
	}
	public String getCepTrabalho() {
		return cepTrabalho;
	}
	public void setCepTrabalho(String cepTrabalho) {
		this.cepTrabalho = cepTrabalho;
	}
	public TipoProfissao getTipoProfissao() {
		return tipoProfissao;
	}
	public void setTipoProfissao(TipoProfissao tipoProfissao) {
		this.tipoProfissao = tipoProfissao;
	}
	
}
